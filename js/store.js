// üö´ ELIMINAR ELEMENTOS GALLERY DEL STORE
function removeGalleryElements() {
  // Eliminar cualquier elemento con ID gallery
  const galleryElements = document.querySelectorAll('#gallery, [id*=\"gallery\"]');
  galleryElements.forEach(element => {
    if (element && element.parentNode) {
      element.remove();
    }
  });
  
  // Eliminar elementos nav que contengan gallery
  const navElements = document.querySelectorAll('.div__nav_btn');
  navElements.forEach(nav => {
    const hasGallery = nav.querySelector('[id*=\"gallery\"]') || 
                      nav.textContent.includes('GALLERY') ||
                      nav.innerHTML.includes('gallery');
    if (hasGallery) {
      nav.remove();
    }
  });
  
  // Eliminar elementos que contengan texto GALLERY
  const allElements = document.querySelectorAll('*');
  allElements.forEach(element => {
    if (element.textContent === 'GALLERY' && 
        !element.closest('.footer-gallery') && 
        !element.closest('.gallery-item')) {
      const parent = element.closest('.div__nav_btn') || element.parentNode;
      if (parent) {
        parent.remove();
      }
    }
  });
}


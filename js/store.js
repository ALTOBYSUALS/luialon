// Reemplaza las im√°genes del footer con productos aleatorios de la tienda

function replaceFooterImagesWithStoreProducts() {
  console.log('üé® Iniciando reemplazo de im√°genes del footer con productos de la tienda...');
  
  // Verificar si los productos est√°n disponibles
  if (!products || products.length === 0) {
    console.warn('‚ö†Ô∏è Productos no disponibles para el footer, reintentando en 500ms...');
    setTimeout(replaceFooterImagesWithStoreProducts, 500);
    return;
  }
  
  console.log(`üõí Productos disponibles para footer: ${products.length}`);
  
  // Seleccionar 6 productos aleatorios para el footer
  const shuffledProducts = [...products].sort(() => 0.5 - Math.random());
  const selectedProducts = shuffledProducts.slice(0, 6);
  
  console.log('üé≤ Productos seleccionados para footer:', selectedProducts.map(p => p.title));
  
  // Seleccionar todas las im√°genes del footer
  const footerImages = document.querySelectorAll('.footer-gallery .gallery-item img');
  
  if (footerImages.length === 0) {
    console.warn('‚ö†Ô∏è No se encontraron im√°genes del footer');
    return;
  }
  
  console.log(`üñºÔ∏è Im√°genes del footer encontradas: ${footerImages.length}`);
  
  // Reemplazar cada imagen del footer
  footerImages.forEach((img, index) => {
    // Usar producto c√≠clico si hay menos productos que im√°genes
    const productIndex = index % selectedProducts.length;
    const product = selectedProducts[productIndex];
    
    console.log(`üîÑ Reemplazando imagen ${index + 1} del footer con ${product.title}`);
    
    // Reemplazar inmediatamente
    img.src = product.imageUrl;
    img.srcset = product.imageUrl;
    img.alt = `${product.title} - ${product.collection}`;
    
    // Agregar datos para navegaci√≥n
    img.dataset.productId = product.id;
    img.dataset.productTitle = product.title;
    img.dataset.storeImage = 'true';
    
    // Agregar evento de click para ir a detalles del producto
    img.addEventListener('click', (e) => {
      e.preventDefault();
      console.log(`üéØ Click en producto del footer: ${product.title} (ID: ${product.id})`);
      window.location.href = `product-details.html?id=${product.id}`;
    });
    
    // Efectos hover
    img.addEventListener('mouseenter', () => {
      img.style.transform = 'scale(1.05)';
      img.style.transition = 'transform 0.3s ease';
      img.style.cursor = 'pointer';
    });
    
    img.addEventListener('mouseleave', () => {
      img.style.transform = 'scale(1)';
    });
    
    console.log(`‚úÖ Imagen ${index + 1} del footer -> ${product.title} (${product.imageUrl})`);
  });
  
  console.log('üéâ Reemplazo de im√°genes del footer completado exitosamente!');
}

